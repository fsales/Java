package br.com.evento.core.servico.exception;

public class RNException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8100340848030466545L;

	public RNException() {

	}

	public RNException(String message) {
		super(message);

	}

	public RNException(Throwable cause) {
		super(cause);

	}

	public RNException(String message, Throwable cause) {
		super(message, cause);

	}

}
